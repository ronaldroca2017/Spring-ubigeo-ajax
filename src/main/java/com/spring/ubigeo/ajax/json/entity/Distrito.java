package com.spring.ubigeo.ajax.json.entity;

public class Distrito {
	
	private Integer idDist;
	private String nomDist;
	private Integer idProv;
	
	
	public Integer getIdDist() {
		return idDist;
	}
	public void setIdDist(Integer idDist) {
		this.idDist = idDist;
	}
	public String getNomDist() {
		return nomDist;
	}
	public void setNomDist(String nomDist) {
		this.nomDist = nomDist;
	}
	public Integer getIdProv() {
		return idProv;
	}
	public void setIdProv(Integer idProv) {
		this.idProv = idProv;
	}
	
	
}
